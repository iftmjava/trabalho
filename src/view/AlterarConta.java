        x.setNome(NomeText.getText());
        x.setEmail(EmailTextField.getText());
      
        try {
            dao.alterar(x);
        } catch (SQLException ex) {
            Logger.getLogger(AlterarConta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dispose();
